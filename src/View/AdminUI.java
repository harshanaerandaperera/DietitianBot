    public void populateUsersList() {
        DefaultTableModel dtm = (DefaultTableModel) tblUserMgt.getModel();
        dtm.setRowCount(0);
        for (User user : users) {
            Vector v = new Vector();
            v.add(user.getName());
            v.add(user.getEmail());
            v.add(user.getAge());
            v.add(user.getHeight());
            v.add(user.getWeight());
            v.add(user.getGender());

            dtm.addRow(v);
        }
    }

